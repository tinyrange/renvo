package main

import (
	rtgos "j5.nz/rtg/rtg/std/os"
)

const projectMainFile = "main.go"
const projectUserFormFile = "main_form.go"
const projectGeneratedFormFile = "main_form_generated.go"
const projectOutputFile = "hello-app"

type helloFormDesign struct {
	title       string
	messageText string
	buttonText  string
	width       int
	height      int
}

type projectActionResult struct {
	message string
	ok      bool
}

func defaultHelloFormDesign() helloFormDesign {
	return helloFormDesign{
		title:       "Hello World",
		messageText: "Click the button to continue.",
		buttonText:  "Say Hello",
		width:       420,
		height:      240,
	}
}

// ensureHelloWorldProject gives an empty directory a complete first Forms
// application. Existing Go projects are never rewritten or supplemented.
func ensureHelloWorldProject(root string) (bool, string) {
	entries, err := rtgos.ReadDir(root)
	if err != nil {
		return false, "Could not read the project directory."
	}
	for i := 0; i < len(entries); i++ {
		name := entries[i].Name()
		if !entries[i].IsDir() && workspaceHasSuffix(name, ".go") {
			return false, ""
		}
	}
	design := defaultHelloFormDesign()
	files := []string{projectMainFile, projectUserFormFile, projectGeneratedFormFile}
	data := [][]byte{helloMainSource(), helloUserFormSource(), generatedHelloFormSource(design)}
	for i := 0; i < len(files); i++ {
		if rtgos.WriteFile(workspaceJoinPath(root, files[i]), data[i], 0644) != nil {
			return false, "Could not create " + files[i] + "."
		}
	}
	return true, "Created a Hello World Forms project."
}

func helloMainSource() []byte {
	return []byte(`package main

import "j5.nz/rtg/rtg/std/graphics"

func main() {
	window := graphics.NewWindow(graphics.WindowOptions{Title: "Hello World", Width: 420, Height: 240})
	if window == nil {
		return
	}
	form := NewMainForm()
	for {
		if form.Paint(window.Surface()) && !window.Present() {
			window.Close()
			return
		}
		event, ok := window.Wait()
		if !ok || event.Type == graphics.EventWindowClose {
			window.Close()
			return
		}
		form.Dispatch(event)
	}
}
`)
}

func helloUserFormSource() []byte {
	return []byte(`package main

// User code belongs in this file. The designer never rewrites it.
func (f *MainForm) helloButtonClick() {
	f.messageLabel.SetText("Hello, World!")
}
`)
}

func generatedHelloFormSource(design helloFormDesign) []byte {
	out := make([]byte, 0, 1800)
	out = append(out, "// Code generated by RTG Forms Designer; DO NOT EDIT.\n"...)
	out = append(out, "// The designer model is this Go source; there is no companion UI format.\n\n"...)
	out = append(out, "package main\n\n"...)
	out = append(out, "import (\n\t\"j5.nz/rtg/rtg/forms\"\n\t\"j5.nz/rtg/rtg/std/graphics\"\n)\n\n"...)
	out = append(out, "type MainForm struct {\n\tforms.Form\n\tuiFont *graphics.Font\n\tmessageLabel *forms.Label\n\thelloButton *forms.Button\n}\n\n"...)
	out = append(out, "func NewMainForm() *MainForm {\n\tform := &MainForm{}\n\tform.initializeComponent()\n\treturn form\n}\n\n"...)
	out = append(out, "func (f *MainForm) initializeComponent() {\n"...)
	out = append(out, "\tf.Form.Initialize("...)
	out = append(out, workspaceDecimal(design.width)...)
	out = append(out, ", "...)
	out = append(out, workspaceDecimal(design.height)...)
	out = append(out, ")\n"...)
	out = append(out, "\tf.Form.SetBackground(graphics.RGBA(250, 251, 253, 255))\n\tf.uiFont = graphics.NewBuiltinFont(2)\n\n"...)
	out = append(out, "\tf.messageLabel = forms.NewLabel()\n\tf.messageLabel.SetBounds(graphics.R(50, 48, 320, 32))\n\tf.messageLabel.SetFont(f.uiFont)\n\tf.messageLabel.SetForeground(graphics.RGBA(32, 36, 42, 255))\n\tf.messageLabel.SetText("...)
	out = appendGoQuoted(out, design.messageText)
	out = append(out, ")\n\n"...)
	out = append(out, "\tf.helloButton = forms.NewButton()\n\tf.helloButton.SetBounds(graphics.R(50, 105, 320, 44))\n\tf.helloButton.SetFont(f.uiFont)\n\tf.helloButton.SetText("...)
	out = appendGoQuoted(out, design.buttonText)
	out = append(out, ")\n\tf.helloButton.Click = f.helloButtonClick\n\n"...)
	out = append(out, "\tf.Form.Add(&f.messageLabel.Control)\n\tf.Form.Add(&f.helloButton.Control)\n}\n"...)
	return out
}

func appendGoQuoted(out []byte, value string) []byte {
	out = append(out, '"')
	for i := 0; i < len(value); i++ {
		c := value[i]
		if c == '\\' || c == '"' {
			out = append(out, '\\')
		}
		if c == '\n' {
			out = append(out, '\\', 'n')
		} else if c == '\r' {
			out = append(out, '\\', 'r')
		} else if c == '\t' {
			out = append(out, '\\', 't')
		} else {
			out = append(out, c)
		}
	}
	out = append(out, '"')
	return out
}

func workspaceJoinPath(root, name string) string {
	if root == "" || root == "." {
		return name
	}
	last := root[len(root)-1]
	if last == '/' || last == '\\' {
		return root + name
	}
	return root + "/" + name
}

func workspaceHasSuffix(value, suffix string) bool {
	if len(value) < len(suffix) {
		return false
	}
	start := len(value) - len(suffix)
	for i := 0; i < len(suffix); i++ {
		if value[start+i] != suffix[i] {
			return false
		}
	}
	return true
}
