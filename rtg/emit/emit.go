package emit

import (
	"strconv"

	"j5.nz/rtg/rtg/unit"
)

func FileName(importPath string) string {
	if importPath == "" {
		return "unit.rtg.go"
	}
	out := make([]byte, 0, len(importPath)+7)
	for i := 0; i < len(importPath); i++ {
		c := importPath[i]
		if (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9') {
			out = append(out, c)
		} else {
			out = append(out, '_')
		}
	}
	out = appendString(out, ".rtg.go")
	return string(out)
}

func Source(u unit.Unit) []byte {
	var out []byte
	out = appendString(out, "//go:build rtg\n")
	out = append(out, '\n')
	out = appendString(out, "// Code generated by rtg; DO NOT EDIT.\n")
	out = appendString(out, "// rtg:unit ")
	out = appendString(out, quoteIfNeeded(u.ImportPath))
	out = append(out, '\n')
	out = appendString(out, "package ")
	out = appendString(out, u.Package)
	out = appendString(out, "\n\n")
	imports := copyStrings(u.Imports)
	sortStrings(imports)
	for i := 0; i < len(imports); i++ {
		imp := imports[i]
		out = appendString(out, "// rtg:import ")
		out = appendString(out, quote(imp))
		out = append(out, '\n')
	}
	exports := copySymbols(u.Exports)
	sortSymbolsByName(exports)
	for i := 0; i < len(exports); i++ {
		sym := exports[i]
		out = appendString(out, "// rtg:export ")
		out = appendString(out, sym.Name)
		out = appendString(out, " => ")
		out = appendString(out, sym.UnitName)
		out = append(out, '\n')
	}
	refs := copySymbols(u.References)
	sortSymbolsByImportPathName(refs)
	for i := 0; i < len(refs); i++ {
		sym := refs[i]
		out = appendString(out, "// rtg:ref ")
		out = appendString(out, quoteIfNeeded(sym.ImportPath))
		out = append(out, ' ')
		out = appendString(out, sym.Name)
		out = appendString(out, " => ")
		out = appendString(out, sym.UnitName)
		out = append(out, '\n')
	}
	if len(imports) > 0 || len(exports) > 0 || len(refs) > 0 {
		out = append(out, '\n')
	}
	for i := 0; i < len(u.Decls); i++ {
		decl := u.Decls[i]
		out = appendString(out, "// rtg:decl ")
		out = appendString(out, decl.Kind)
		if decl.Name != "" {
			out = append(out, ' ')
			out = appendString(out, decl.Name)
		}
		if decl.UnitName != "" {
			out = appendString(out, " => ")
			out = appendString(out, decl.UnitName)
		}
		out = appendString(out, " ")
		out = appendString(out, quoteIfNeeded(decl.Path))
		out = append(out, '\n')
		out = appendString(out, decl.Body)
		if len(decl.Body) == 0 || decl.Body[len(decl.Body)-1] != '\n' {
			out = append(out, '\n')
		}
		if i+1 < len(u.Decls) {
			out = append(out, '\n')
		}
	}
	return out
}

func appendString(out []byte, s string) []byte {
	for i := 0; i < len(s); i++ {
		out = append(out, s[i])
	}
	return out
}

func copyStrings(values []string) []string {
	out := make([]string, len(values))
	for i := 0; i < len(values); i++ {
		out[i] = values[i]
	}
	return out
}

func copySymbols(values []unit.Symbol) []unit.Symbol {
	out := make([]unit.Symbol, len(values))
	for i := 0; i < len(values); i++ {
		out[i] = values[i]
	}
	return out
}

func quote(s string) string {
	return strconv.Quote(s)
}

func quoteIfNeeded(s string) string {
	for i := 0; i < len(s); i++ {
		if s[i] == ' ' || s[i] == '\t' || s[i] == '\r' || s[i] == '\n' || s[i] == '"' || s[i] == '\\' {
			return quote(s)
		}
	}
	return s
}

func sortStrings(values []string) {
	for i := 1; i < len(values); i++ {
		value := values[i]
		j := i - 1
		for j >= 0 && values[j] > value {
			values[j+1] = values[j]
			j = j - 1
		}
		values[j+1] = value
	}
}

func sortSymbolsByName(symbols []unit.Symbol) {
	for i := 1; i < len(symbols); i++ {
		value := symbols[i]
		j := i - 1
		for j >= 0 && symbolAfterByName(symbols[j], value) {
			symbols[j+1] = symbols[j]
			j = j - 1
		}
		symbols[j+1] = value
	}
}

func symbolAfterByName(a unit.Symbol, b unit.Symbol) bool {
	if a.Name == b.Name {
		return a.UnitName > b.UnitName
	}
	return a.Name > b.Name
}

func sortSymbolsByImportPathName(symbols []unit.Symbol) {
	for i := 1; i < len(symbols); i++ {
		value := symbols[i]
		j := i - 1
		for j >= 0 && symbolAfterByImportPathName(symbols[j], value) {
			symbols[j+1] = symbols[j]
			j = j - 1
		}
		symbols[j+1] = value
	}
}

func symbolAfterByImportPathName(a unit.Symbol, b unit.Symbol) bool {
	if a.ImportPath == b.ImportPath {
		return a.Name > b.Name
	}
	return a.ImportPath > b.ImportPath
}
